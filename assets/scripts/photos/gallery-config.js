// Fonction pour obtenir la configuration d'une galerie
function getGalleryConfig(galleryId) {
  return galleryConfigs[galleryId];
}

// Fonction pour obtenir toutes les galeries triées par date
function getAllGalleriesSorted() {
  const galleries = [];
  
  for (const [id, config] of Object.entries(galleryConfigs)) {
    galleries.push({
      id: id,
      title: config.title,
      date: config.date,
      src: `${id}.html`,
      local: true
    });
  }
  
  // Trier par date (plus récent en premier)
  return galleries.sort((a, b) => new Date(b.date) - new Date(a.date));
}

// Fonction pour grouper les galeries par année
function getGalleriesByYear() {
  const galleries = getAllGalleriesSorted();
  const grouped = {};
  
  galleries.forEach(gallery => {
    const year = new Date(gallery.date).getFullYear();
    if (!grouped[year]) {
      grouped[year] = [];
    }
    grouped[year].push(gallery);
  });
  
  return grouped;
} 